# Operacoes aritmeticas
a = 10
b = 3

soma = a + b  # 13
subtracao = a - b  # 7
multiplicacao = a * b  # 30
divisao = a / b  # 3.333333333
divisao_inteira = a // b  # 3
modulo = a % b  # 1
exponenciacao = a ** b  # 1000

print(soma)
print(subtracao)
print(multiplicacao)
print(divisao)
print(divisao_inteira)
print(modulo)
print(exponenciacao)
